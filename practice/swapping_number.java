package com.practice;

public class swapping_number 
{

	public static void main(String[] args) 
	{
		int a = 25;
		int b = 24;
		System.out.println("Before Swapping a is " + a +" and b is " + b);
		int c=a;
		    a=b;
		    b=c;
		System.out.println("SWAPPED NUMBER a is " + a +" and b is " + b);
	}

}
